# logical operators

print(True and True)
print(True and False)
print(False and False)
print(True or True)
print(True or False)
print(False or False)

xor = True ^ True
print(xor)
xor = True ^ False
print(xor)
xor = False ^ False
print(xor)


# bitwise Operator

num1 = 10
num2 = 20

print(num1 & num2)
print(num1 | num2)
print(num1 ^ num2)
print(num1 << 2)
print(num1 >> 2)
